// JavaScript Loops
// Loops are handy, if you want to run the same code over and over again, each time with a different value.

// Often this is the case when working with arrays:

let text="";
for (let i = 0; i < 5; i++) {
    text += "The number is " + i + "<br>";
    console.log(i);
    

  }

console.log(text);